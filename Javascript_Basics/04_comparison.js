// *******Simple Comparison*********

console.log(2 > 1);
console.log(2 >= 1);
console.log(2 < 1);
console.log(2 == 1);
console.log(2 != 1);

// *********Different dataTypes*********

console.log("2" > 1);
console.log("02" > 1);


//***********NULL*********** 

console.log(null > 0);
console.log(null == 0);
console.log(null >= 0);


//**********Undefined*********

console.log(undefined == 0);
console.log(undefined > 0);
console.log(undefined < 0);

// ==== Strict Equality

console.log("2" === 2);
